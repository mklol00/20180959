package study_02;

public class _03_Practice {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;
        int c = 3;
        int d = 6;
        System.out.println(++a);
        System.out.println(b++);
        System.out.println(c--);
        System.out.println(--d);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
